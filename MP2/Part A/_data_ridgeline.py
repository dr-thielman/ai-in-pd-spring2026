            "for production volumes above 500 units.\n\n"
            "2.3 Monte Carlo Simulation\n"
            "Numerical simulation using 10,000+ random samples drawn from actual "
            "or assumed distributions. Required for non-normal distributions, "
            "assemblies with more than 8 contributing dimensions, and cases where "
            "Cpk targets must be predicted. Ridgeline uses Crystal Ball or custom "
            "Python scripts for Monte Carlo analysis.\n\n"
